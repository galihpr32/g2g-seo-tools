// ── Sprint #380 BOSS.VIEW.PDF — HTML template for the Weekly Snapshot PDF ──
//
// Renders a polished, multi-page A4 PDF that mirrors the visual quality of
// the social team's existing Weekly Social Media Report. Key differences vs
// the screen UI in BossViewContent:
//
//   • Light theme — white bg, dark text (#0F1218), high-contrast bar colors.
//     Print-shop style; dark themes look muddy in PDFs.
//   • Branded chrome on EVERY page via CSS `@page` margin boxes
//     (top-left + top-right strings, bottom-left + bottom-right strings,
//     red 4px stripe under the header). Margin boxes are honored natively
//     by Chromium's `page.pdf({ preferCSSPageSize: true })`.
//   • Bright bar charts with value labels above each bar (every 4th bar
//     to avoid crowding). G2G uses #DC2626 red, OffGamers uses #2563EB
//     blue. Revenue overlay = green #10B981 line.
//   • KPI presented as a flat table (no floating cards) with alternating
//     row shading — easier to skim than 4 small cards.
//   • Commentary as full-bleed callouts: green ✓ Why it worked, amber
//     → Action taken. Optional red ! Watch list when commentary suggests
//     one (currently driven only by negative WoW signals in commentary
//     text; no separate field yet).
//   • Methodology footer + page-break-inside rules so charts don't split
//     awkwardly between A4 pages.
//
// The template is a single big function returning an HTML string. Inline
// CSS + inline Chart.js script + a small data block. Puppeteer:
//   1. setContent(html) → loads the page
//   2. waits for `window.kpiReady === true` (the script sets this after
//      all charts paint a single frame)
//   3. page.pdf({ printBackground: true, displayHeaderFooter: false })
//
// We reuse `window.kpiReady` instead of inventing a new signal so the same
// ready timeout logic in `htmlToPdf` (puppeteer-launcher.ts) works without
// branching.

import type { BossViewPayload } from './boss-view'

