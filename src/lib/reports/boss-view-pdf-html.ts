// ── Tiny helpers (no `any`) ───────────────────────────────────────────────

function escapeHtml(raw: string): string {
  return raw
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;')
}

function fmtNum(n: number): string {
  if (!Number.isFinite(n)) return '—'
  if (Math.abs(n) >= 1_000_000) return `${(n/1_000_000).toFixed(1)}M`
  if (Math.abs(n) >= 1_000)     return `${(n/1_000).toFixed(1)}K`
  return Math.round(n).toLocaleString()
}

function fmtUsd(n: number): string {
  if (!Number.isFinite(n)) return '—'
  if (Math.abs(n) >= 1_000_000) return `$${(n/1_000_000).toFixed(1)}M`
  if (Math.abs(n) >= 1_000)     return `$${(n/1_000).toFixed(0)}K`
  return `$${Math.round(n)}`
}

function fmtPct(v: number | null | undefined): string {
  if (v == null || !Number.isFinite(v)) return '—'
  if (Math.abs(v) < 1) return 'flat'
  return v > 0 ? `+${v.toFixed(0)}%` : `${v.toFixed(0)}%`
}

function pctColor(v: number | null | undefined): string {
  if (v == null || !Number.isFinite(v)) return PALETTE.inkMuted
  if (Math.abs(v) < 1) return PALETTE.inkMuted
  return v > 0 ? PALETTE.positive : PALETTE.negative
}

function shortDate(ymd: string): string {
  if (!ymd) return ''
  const d = new Date(ymd + 'T00:00:00')
  return d.toLocaleDateString('en-US', { month: 'short', day: 'numeric' })
}

function brandColor(slug: string): string {
  return slug === 'g2g' ? PALETTE.brandRed : PALETTE.brandBlue
}

